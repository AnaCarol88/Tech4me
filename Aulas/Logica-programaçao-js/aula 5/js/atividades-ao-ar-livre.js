function atividades_ao_ar_livre() {

    var temperatura = prompt ("Qual é a temperatura de hoje?");
    var clima = prompt ("Está chovendo? Se sim coloque um");

    if (clima = "não" && temperatura < 32 ) {
        alert ("Adequado para atividade ao ar livre");
    } else {
        alert ("Não adequado para atividade ao ar livre");
     
    }

}

atividades_ao_ar_livre ();